    private void drawFigure(Graphics2D g,int x1, int y1, int x2, int y2) {
         
         int x, y; 
         int w, h; 
         if (x1 >= x2) {  // x2 is left edge
            x = x2;
            w = x1 - x2;
         }
         else {          // x1 is left edge
            x = x1;
            w = x2 - x1;
         }
         if (y1 >= y2) {  // y2 is top edge
            y = y2;
            h = y1 - y2;
         }
         else {          // y1 is top edge.
            y = y1;
            h = y2 - y1;
         }
         g.setPaint(DrawColor);
         g.setStroke(new BasicStroke(Size));
         if(Shape!=0)
         {
            switch(Shape)
            {case 5:
             case 1:g.draw(new Line2D.Double(x1,y1,x2,y2));                     break;
             case 2:if(fil==0)
                     g.draw(new Ellipse2D.Double(x,y,w,h));
                    else 
                     g.fill(new Ellipse2D.Double(x,y,w,h));                     break;
             case 3:if(fil==0)
                     g.draw(new Rectangle2D.Double(x,y,w,h));   
                    else
                     g.fill(new Rectangle2D.Double(x,y,w,h));                   break;
             case 4:if(fil==0)
                     g.draw(new RoundRectangle2D.Double(x,y,w,h,20,20));
                    else
                     g.fill(new RoundRectangle2D.Double(x,y,w,h,20,20));        break;
             /*case 5:Gcanvas.setStroke(new BasicStroke(Size));
                    Gcanvas.setPaint(DrawColor);
                    Gcanvas.draw(new Line2D.Double(x1,y1,x2,y2));
                    Gimg.setStroke(new BasicStroke(Size));
                    Gimg.setPaint(DrawColor);
                    Gimg.draw(new Line2D.Double(x1,y1,x2,y2));                  break;*/
            }
         }
         else if(Tool!=0)
         {
            switch(Tool)
            {
             case 1:g.draw(new Line2D.Double(x1,y1,x2,y2));                     break;
             case 2:g.setPaint(WHITE);
                    g.fill(new Rectangle2D.Double(x2,y2,Size*2,Size*2));        break;
             case 4:Color bkcolor=getPixel(CanvasImg,x2,y2);
                    Color pxcolor = bkcolor;
                    int crx=x2,cry=y2;
                    
             case 5:Random r=new Random();
                    for(int i=0;i<100;++i){
                        g.fill(new Rectangle2D.Double(r.nextInt((int)(Size*2))+x2,r.nextInt((int)Size*2)+y2,1,1));
                    }                                                           break;
             case 6:DrawColor=getPixel(CanvasImg,x2,y2);
                    ShowCrntColor();                                            break;
            } 
         }
    }
    public Color getPixel(Image i,int x,int y)
    {   BufferedImage image=(BufferedImage)i;
        int clr=image.getRGB(x, y);
        int red=(clr&0x00ff0000)>>16;
        int green=(clr&0x0000ff00)>>8;
        int blue=clr&0x000000ff;
        System.out.println("Color:("+red+","+green+","+blue+");");
        Color Clr=new Color(red,green,blue);
        return Clr;
    }
