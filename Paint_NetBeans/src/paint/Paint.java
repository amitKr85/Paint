    private void canvasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasMousePressed
        sx=px=evt.getX();
        sy=py=evt.getY();
        Gimg=(Graphics2D)CanvasImg.getGraphics();
        if(Tool==3)
        {
            Frame_Text.show();
            Frame_Text.setBounds(0,0,400,500);
        }
        else if(Tool==4)
        {
            drawFigure(Gcanvas,px,py,sx,sy);
        }
        else if(Tool==6)
        {
            drawFigure(Gcanvas,px,py,sx,sy);
        }
    }//GEN-LAST:event_canvasMousePressed

    private void canvasMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasMouseReleased
        if(Shape!=0)
        {   drawFigure(Gcanvas,sx,sy,x,y);
            drawFigure(Gimg,sx,sy,x,y);
        }
        else if(Tool!=0&&Tool!=4)
        {   
            
            drawFigure(Gcanvas,px,py,x,y);
            drawFigure(Gimg,px,py,x,y);
        }
            Gimg.dispose();
        Gimg=null;
    }//GEN-LAST:event_canvasMouseReleased

    private void canvasMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasMouseDragged
        x=evt.getX();
        y=evt.getY();
        Gcanvas.drawImage(CanvasImg,0,0,this);
        if(Shape!=0&&Shape!=5)
        drawFigure(Gcanvas,sx,sy,x,y);
        else if(Shape==5)
        {
            drawFigure(Gcanvas,sx,sy,x,y);
            drawFigure(Gimg,sx,sy,x,y);
        }
        else if(Tool!=0&&Tool!=4)
        { 
            drawFigure(Gcanvas,px,py,x,y);
            drawFigure(Gimg,px,py,x,y);      
        }    
        px=x;
        py=y;
    }//GEN-LAST:event_canvasMouseDragged

